public class methodreturn 
{
   // This example returns the sum of a method parameters and 5

  public static int additionCal(int x) 
  {
    return 5 + x;
  }

  public static void main(String[] args) 
  {
    System.out.println(additionCal(10));
    System.out.println(additionCal(15));
    System.out.println(additionCal(14));
    System.out.println(additionCal(34));
  }
}


